    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        _facturaEncontrada = MDIPrincipal.gFactura.BuscarPorIdGetElem(_codigoFactura);
        if (_facturaEncontrada == null) { //Si no existe, se registrara
            if (_clienteFactura == null) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Ingrese el cÃ³digo del cliente");
            } else {
                if (_asesorServFactura == null) {
                    Toolkit.getDefaultToolkit().beep();
                    JOptionPane.showMessageDialog(this, "Ingrese el cÃ³digo del asesor");
                } else {
                    if (_boletoFactura == null) {
                        Toolkit.getDefaultToolkit().beep();
                        JOptionPane.showMessageDialog(this, "Ingrese el cÃ³digo del boleto");
                    } else {
                        btnBuscarAsesor.setEnabled(false);
                        btnBuscarCliente.setEnabled(false);

                        _facturaEncontrada = new Factura();
                        _facturaEncontrada.set_id(_codigoFactura);
                        _facturaEncontrada.set_aerolinea(_aerolineaFactura);
                        _facturaEncontrada.set_asesorServ(_asesorServFactura);
                        _facturaEncontrada.set_cliente(_clienteFactura);
                        _facturaEncontrada.set_fecha(AdminFechas.FechaToString(_fecha));
                        MDIPrincipal.gFactura.Agregar(_facturaEncontrada);

                    }
                }
            }

        }
        _posicionFactura = MDIPrincipal.gFactura.BuscarPorId(_facturaEncontrada.get_id());
        DetalleFactura _nuevoDetalle = new DetalleFactura();
        _nuevoDetalle.set_boleto(_boletoFactura);
        _nuevoDetalle.set_subtotal(_boletoFactura.get_categoriaVuelo().get_precio());

        boolean _resultado = _facturaEncontrada.agregarDetalle(_nuevoDetalle);
        _facturaEncontrada.set_subtotal(_facturaEncontrada.ObtenerSubTotal());
        _facturaEncontrada.set_isv(_facturaEncontrada.ObtenerSubTotal_ISV());
        _facturaEncontrada.set_total(_facturaEncontrada.ObtenerTotal());
        MDIPrincipal.gFactura.Modificar(_posicionFactura, _facturaEncontrada);
        MDIPrincipal.frmFacturacion.actualizarElementosTabla();
        AdminSerializacion.serializacion(MDIPrincipal.gFactura, "gFactura.obj");
        actualizarElementosTabla();


    }//GEN-LAST:event_btnGuardarActionPerformed

    public void actualizarElementosTabla() {
        String[] titulos = {"Pasajero", "Origen // Destino", "Categoria de Vuelo", "Asiento", "SubTotal"};

        DefaultTableModel dt = new DefaultTableModel(this._facturaEncontrada.getArrayDetalleFactura(), titulos);

        this.jtFacturacionBoletos.setModel(dt);
    }

